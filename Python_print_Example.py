# anythng in single or double quote is string
print("Hello World!!")  #Hello World!!
print('Hello') #Hello 
print("Haven't") # Haven't
print('haven\'t') #haven't
print('haven"t') #haven"t
person = "Print"
print("person")  # person
# whatever given in double quote will be printed as it is.
print(person) # Print
print("AB", end="")
print("CD", end="")
print("A", end="..*..")
print("K")
# Output : ABCDA..*..K
print("C:\\new_folder\\test.txt")
# raw string, no processing -> comment
